public class ForloopExample {
    public static void main(String[] args) {
        System.out.println("Numbers from (1-10): ");
        for(int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

System.out.println("\nTable of 5:");
        for (int i = 1; i <= 10; i++) {
            System.out.println("5 x " + i + " = " + (5 * i));
        }
    }
}
    
